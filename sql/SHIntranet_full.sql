-- 현재 결재 순서 조회 함수
CREATE OR REPLACE FUNCTION FN_GET_APRV_CURRENT_FLOW(P_DOC_CD IN SH_DOCUMENT.DOC_CD%TYPE)
RETURN NUMBER
IS
    V_CURRENT_FLOW NUMBER;
BEGIN
    SELECT NVL(MIN(AL.FLOW), 0) INTO V_CURRENT_FLOW
    FROM APRV_LINE AL
    WHERE AL.DOC_CD = P_DOC_CD
      AND NOT EXISTS (
          SELECT 1 FROM APRV_LOG
          WHERE DOC_CD = AL.DOC_CD AND EMP_CD = AL.EMP_CD
      );
    RETURN V_CURRENT_FLOW;
EXCEPTION
    WHEN OTHERS THEN RETURN 0;
END FN_GET_APRV_CURRENT_FLOW;
/


