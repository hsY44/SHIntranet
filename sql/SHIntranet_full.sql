-- ── VIEW_EMPLOYEE : 재직 중인 직원 통합 뷰 ──────────────────
-- 퇴사자는 EMP_INFO 삭제 → INNER JOIN 으로 자동 제외
CREATE OR REPLACE VIEW VIEW_EMPLOYEE AS
SELECT A.EMP_CD, A.EMP_NAME, A.DEPT_CD
     , B.DEPT_NAME, A.POSITION_CD, D.POSITION_NAME
     , D.GRADE, C.TEL, C.EMAIL
     , C.ADDR, C.PWD, C.HIRE_DATE, A.REG_DT
FROM EMP A
JOIN DEPT         B ON A.DEPT_CD      = B.DEPT_CD
JOIN EMP_INFO     C ON A.EMP_CD       = C.EMP_CD
JOIN EMP_POSITION D ON A.POSITION_CD  = D.POSITION_CD;

-- ── VIEW_ATTEND_LOG : 출퇴근 기록 통합 뷰 ──────────────────
CREATE OR REPLACE VIEW VIEW_ATTEND_LOG AS
SELECT AL.LOG_NO
     , ATYPE.TYPE_CD
     , ATYPE.TYPE_NAME
     , AL.EMP_CD
     , E.EMP_NAME
     , E.DEPT_CD
     , D.DEPT_NAME
     , E.POSITION_CD
     , P.POSITION_NAME
     , TO_CHAR(AL.REG_DT, 'YYYY-MM-DD HH24:MI:SS') AS REG_DT
FROM ATTEND_LOG    AL
JOIN ATTEND_TYPE   ATYPE ON AL.TYPE_CD    = ATYPE.TYPE_CD
JOIN EMP           E     ON AL.EMP_CD     = E.EMP_CD
JOIN DEPT          D     ON E.DEPT_CD     = D.DEPT_CD
JOIN EMP_POSITION  P     ON E.POSITION_CD = P.POSITION_CD;

-- ── VIEW_WORK_SEARCH : 회사 업무 검색 뷰 ───────────────────
CREATE OR REPLACE VIEW VIEW_WORK_SEARCH AS
SELECT ROW_NUMBER() OVER(ORDER BY W.REG_DT DESC) AS ROW_NUM
     , WT.TYPE_NAME
     , W.WORK_NAME
     , TO_CHAR(W.REG_DT, 'YYYY-MM-DD') AS REG_DT
     , E.EMP_CD
     , D.DEPT_NAME
     , E.EMP_NAME
     , P.POSITION_NAME
     , W.WORK_CD
     , W.TYPE_CD
FROM WORK          W
JOIN WORK_TYPE     WT ON W.TYPE_CD    = WT.TYPE_CD
JOIN EMP           E  ON W.EMP_CD     = E.EMP_CD
JOIN DEPT          D  ON E.DEPT_CD    = D.DEPT_CD
JOIN EMP_POSITION  P  ON E.POSITION_CD = P.POSITION_CD;

-- ── VIEW_DEPT_WORK_SEARCH : 부서 업무 검색 뷰 ──────────────
CREATE OR REPLACE VIEW VIEW_DEPT_WORK_SEARCH AS
SELECT D.DEPT_NAME
     , WT.TYPE_NAME
     , W.WORK_NAME
     , WDR.FLOW
FROM WORK_DEPT_RULE WDR
JOIN WORK           W  ON WDR.WORK_CD = W.WORK_CD
JOIN WORK_TYPE      WT ON W.TYPE_CD   = WT.TYPE_CD
JOIN DEPT           D  ON WDR.DEPT_CD = D.DEPT_CD;

-- ── VIEW_WORK_TARGET : 업무 지시 가능 대상자 뷰 ─────────────
CREATE OR REPLACE VIEW VIEW_WORK_TARGET AS
SELECT E.EMP_CD
     , E.EMP_NAME
     , P.POSITION_NAME
     , E.DEPT_CD
     , P.GRADE
FROM EMP           E
JOIN EMP_POSITION  P  ON E.POSITION_CD = P.POSITION_CD
JOIN EMP_INFO      I  ON E.EMP_CD      = I.EMP_CD;  -- 재직자만

-- ── VIEW_MANAGER : 업무 담당자 통합 뷰 ─────────────────────
CREATE OR REPLACE VIEW VIEW_MANAGER AS
SELECT ROW_NUMBER() OVER(ORDER BY M.MANAGER_NO) AS ROW_NUM
     , M.MANAGER_NO
     , WT.TYPE_NAME
     , W.WORK_NAME
     , D.DEPT_CD
     , D.DEPT_NAME
     , M.MANAGER_EMP_CD
     , E1.EMP_NAME AS MANAGER_EMP_NAME
     , (SELECT MIN(ISSUE_DATE) FROM MANAGER
        WHERE WORK_CD = M.WORK_CD AND MANAGER_EMP_CD = M.MANAGER_EMP_CD) AS S_ISSUE_DATE
     , (SELECT MAX(ISSUE_DATE) FROM MANAGER
        WHERE WORK_CD = M.WORK_CD AND MANAGER_EMP_CD = M.MANAGER_EMP_CD) AS E_ISSUE_DATE
     , M.REG_EMP_CD
     , E2.EMP_NAME AS REG_EMP_NAME
     , M.WORK_CD
FROM MANAGER       M
JOIN WORK          W  ON M.WORK_CD         = W.WORK_CD
JOIN WORK_TYPE     WT ON W.TYPE_CD         = WT.TYPE_CD
JOIN EMP           E1 ON M.MANAGER_EMP_CD  = E1.EMP_CD
JOIN EMP           E2 ON M.REG_EMP_CD      = E2.EMP_CD
JOIN DEPT          D  ON E1.DEPT_CD        = D.DEPT_CD
WHERE M.ISSUE_TYPE = 'S'
  AND NOT EXISTS (
      SELECT 1 FROM MANAGER M2
      WHERE M2.WORK_CD       = M.WORK_CD
        AND M2.MANAGER_EMP_CD = M.MANAGER_EMP_CD
        AND M2.ISSUE_TYPE    = 'E'
        AND M2.ISSUE_DATE    > M.ISSUE_DATE
  );

-- ── VIEW_APRV_SUBMIT_LIST : 결재 상신 목록 뷰 ──────────────
CREATE OR REPLACE VIEW VIEW_APRV_SUBMIT_LIST AS
SELECT SD.DOC_CD
     , SD.EMP_CD
     , W.WORK_NAME
     , SD.TITLE
     , TO_CHAR(SD.REG_DT, 'YYYY-MM-DD') AS REG_DT
     , SD.TYPE_CD
     , DT.TYPE_NAME
     , (SELECT COUNT(*) FROM SH_DOCUMENT WHERE PARENT_DOC_CD = SD.DOC_CD) AS CHILD_CNT
     , CASE
           WHEN NOT EXISTS (SELECT 1 FROM APRV_LINE WHERE DOC_CD = SD.DOC_CD) THEN '결재불필요'
           WHEN NOT EXISTS (SELECT 1 FROM APRV_LOG  WHERE DOC_CD = SD.DOC_CD) THEN '결재대기'
           WHEN EXISTS (SELECT 1 FROM APRV_LOG WHERE DOC_CD = SD.DOC_CD AND TYPE_CD = '002') THEN '반려'
           WHEN (SELECT COUNT(*) FROM APRV_LINE WHERE DOC_CD = SD.DOC_CD) =
                (SELECT COUNT(*) FROM APRV_LOG  WHERE DOC_CD = SD.DOC_CD AND TYPE_CD IN ('001','003')) THEN '결재완료'
           ELSE '결재중'
       END AS STATUS
FROM SH_DOCUMENT SD
JOIN WORK         W  ON SD.WORK_CD = W.WORK_CD
JOIN DOC_TYPE     DT ON SD.TYPE_CD = DT.TYPE_CD
WHERE SD.PARENT_DOC_CD IS NOT NULL;

-- ── VIEW_APRV_RECEIVE_WAIT : 결재 대기 수신 목록 ────────────
CREATE OR REPLACE VIEW VIEW_APRV_RECEIVE_WAIT AS
SELECT SD.DOC_CD
     , SD.PARENT_DOC_CD
     , W.WORK_NAME
     , PD.TITLE
     , SD.EMP_CD
     , E.EMP_NAME
     , TO_CHAR(SD.REG_DT, 'YYYY-MM-DD HH24:MI') AS APRV_REG_DT
     , DT.TYPE_NAME
     , AL.EMP_CD AS APRV_EMP_CD
     , AE.EMP_NAME AS APRV_EMP_NAME
FROM SH_DOCUMENT SD
JOIN APRV_LINE   AL ON SD.DOC_CD   = AL.DOC_CD
JOIN EMP         AE ON AL.EMP_CD   = AE.EMP_CD
JOIN EMP         E  ON SD.EMP_CD   = E.EMP_CD
JOIN WORK        W  ON SD.WORK_CD  = W.WORK_CD
JOIN DOC_TYPE    DT ON SD.TYPE_CD  = DT.TYPE_CD
JOIN SH_DOCUMENT PD ON SD.PARENT_DOC_CD = PD.DOC_CD
WHERE NOT EXISTS (
    SELECT 1 FROM APRV_LOG
    WHERE DOC_CD = SD.DOC_CD AND EMP_CD = AL.EMP_CD
);

-- ── VIEW_APRV_RECEIVE_COMPLETE : 결재 완료 수신 목록 ────────
CREATE OR REPLACE VIEW VIEW_APRV_RECEIVE_COMPLETE AS
SELECT SD.DOC_CD
     , SD.PARENT_DOC_CD
     , W.WORK_NAME
     , PD.TITLE
     , SD.EMP_CD
     , E.EMP_NAME
     , ALOG.TYPE_CD AS APRV_TYPE_CD
     , AT2.TYPE_NAME AS APRV_TYPE_NAME
     , TO_CHAR(ALOG.REG_DT, 'YYYY-MM-DD') AS REG_DT
     , DT.TYPE_NAME AS DOC_TYPE_NAME
     , ALOG.EMP_CD AS APRV_EMP_CD
     , CASE
           WHEN ALOG.TYPE_CD = '002' THEN '반려'
           WHEN ALOG.TYPE_CD IN ('001','003') THEN '결재완료'
           ELSE '결재중'
       END AS STATUS
FROM SH_DOCUMENT SD
JOIN APRV_LOG    ALOG ON SD.DOC_CD   = ALOG.DOC_CD
JOIN APRV_TYPE   AT2  ON ALOG.TYPE_CD = AT2.TYPE_CD
JOIN EMP         E    ON SD.EMP_CD   = E.EMP_CD
JOIN WORK        W    ON SD.WORK_CD  = W.WORK_CD
JOIN DOC_TYPE    DT   ON SD.TYPE_CD  = DT.TYPE_CD
JOIN SH_DOCUMENT PD   ON SD.PARENT_DOC_CD = PD.DOC_CD;


