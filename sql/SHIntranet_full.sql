-- 12. 결재 문서 등록 (결재 라인 포함)
CREATE OR REPLACE PROCEDURE PRC_APRV_DOC_INSERT (
     P_PARENT_CD   IN  SH_DOCUMENT.PARENT_DOC_CD%TYPE
    ,P_DOC_TYPE_CD IN  SH_DOCUMENT.TYPE_CD%TYPE
    ,P_WORK_CD     IN  SH_DOCUMENT.WORK_CD%TYPE
    ,P_EMP_CD      IN  SH_DOCUMENT.EMP_CD%TYPE
    ,P_TITLE       IN  SH_DOCUMENT.TITLE%TYPE
    ,P_CONTENT     IN  SH_DOCUMENT.CONTENTS%TYPE
    ,P_DOC_CD      OUT VARCHAR2
)
IS
    V_DOC_CD VARCHAR2(10);
    V_FLOW   NUMBER := 0;
BEGIN
    SELECT 'DC' || LPAD(DOC_SEQ.NEXTVAL, 4, '0') INTO V_DOC_CD FROM DUAL;

    INSERT INTO SH_DOCUMENT(DOC_CD, PARENT_DOC_CD, TYPE_CD, WORK_CD, EMP_CD, TITLE, CONTENTS, REG_DT)
    VALUES (V_DOC_CD, P_PARENT_CD, P_DOC_TYPE_CD, P_WORK_CD, P_EMP_CD, P_TITLE, P_CONTENT, SYSDATE);

    -- 해당 업무 흐름(WORK_DEPT_RULE)에서 결재 라인 자동 생성
    FOR R IN (
        SELECT E.EMP_CD, WDR.FLOW
        FROM WORK_DEPT_RULE WDR
        JOIN EMP E ON E.DEPT_CD = WDR.DEPT_CD
        JOIN EMP_INFO I ON E.EMP_CD = I.EMP_CD
        WHERE WDR.WORK_CD = P_WORK_CD
        ORDER BY WDR.FLOW
    ) LOOP
        INSERT INTO APRV_LINE(DOC_CD, EMP_CD, FLOW)
        VALUES (V_DOC_CD, R.EMP_CD, R.FLOW);
    END LOOP;

    P_DOC_CD := V_DOC_CD;
    COMMIT;
EXCEPTION
    WHEN OTHERS THEN P_DOC_CD := ''; ROLLBACK; RAISE;
END PRC_APRV_DOC_INSERT;
/

