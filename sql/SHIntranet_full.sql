CREATE SEQUENCE WORK_DEPT_RULE_SEQ    START WITH 1  INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE WORK_TARGET_SEQ       START WITH 1  INCREMENT BY 1 NOCACHE;

