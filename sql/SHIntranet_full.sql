CREATE SEQUENCE POSITION_HISTORY_SEQ  START WITH 6  INCREMENT BY 1 NOCACHE;
-- ^ PH01~PH05 초기 데이터 이미 존재 → 6부터 시작

-- 직원
CREATE SEQUENCE EMP_SEQ               START WITH 1  INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE EXIT_EMP_INFO_SEQ     START WITH 1  INCREMENT BY 1 NOCACHE;

-- 출퇴근
