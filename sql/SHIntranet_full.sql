CREATE OR REPLACE PROCEDURE EMP_LOGIN (
     E_EMP_CD       IN  EMP_INFO.EMP_CD%TYPE
    ,E_EMP_PWD      IN  EMP_INFO.PWD%TYPE
    ,E_EMP_BUSEO_CD IN  EMP.DEPT_CD%TYPE
    ,E_EMP_RESULT   OUT VARCHAR2
    ,E_EMP_NAME     OUT EMP.EMP_NAME%TYPE
)
IS
    V_ID_MATCH NUMBER;
    V_PW_MATCH NUMBER;
BEGIN
    SELECT COUNT(*) INTO V_ID_MATCH FROM EMP_INFO WHERE EMP_CD = E_EMP_CD;

    IF V_ID_MATCH = 0 THEN
        E_EMP_RESULT := 'ID_NOT_FOUND'; E_EMP_NAME := NULL; RETURN;
    END IF;

    SELECT COUNT(*) INTO V_PW_MATCH
    FROM EMP_INFO WHERE EMP_CD = E_EMP_CD AND PWD = E_EMP_PWD;

    IF V_PW_MATCH = 0 THEN
        E_EMP_RESULT := 'WRONG_PASSWORD'; E_EMP_NAME := NULL; RETURN;
    END IF;

    SELECT E.EMP_NAME INTO E_EMP_NAME
    FROM EMP E JOIN EMP_INFO I ON E.EMP_CD = I.EMP_CD
    WHERE E.EMP_CD = E_EMP_CD;

    E_EMP_RESULT := 'SUCCESS';
EXCEPTION
    WHEN OTHERS THEN E_EMP_RESULT := 'ERROR'; E_EMP_NAME := NULL;
END EMP_LOGIN;
