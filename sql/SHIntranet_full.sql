-- 9. 부서 등록/수정/삭제 통합 프로시저
CREATE OR REPLACE PROCEDURE DEPT_HISTORY_CUD (
     E_TYPE      IN VARCHAR2
    ,E_DEPT_CD   IN DEPT.DEPT_CD%TYPE
    ,E_DEPT_NAME IN DEPT.DEPT_NAME%TYPE
)
IS
    V_DEPT_CD    DEPT.DEPT_CD%TYPE;
    V_NAME_COUNT NUMBER;
BEGIN
    IF E_TYPE = 'INSERT' THEN
        SELECT COUNT(*) INTO V_NAME_COUNT FROM DEPT WHERE DEPT_NAME = E_DEPT_NAME;
        IF V_NAME_COUNT > 0 THEN
            RAISE_APPLICATION_ERROR(-20001, '이미 존재하는 부서명입니다.');
        END IF;
        V_DEPT_CD := 'DE' || LPAD(DEPT_SEQ.NEXTVAL, 2, '0');
        INSERT INTO DEPT(DEPT_CD, DEPT_NAME) VALUES(V_DEPT_CD, E_DEPT_NAME);
        INSERT INTO DEPT_HISTORY(HISTORY_CD, DEPT_CD, DEPT_NAME)
        VALUES ('DH' || LPAD(DEPT_HISTORY_SEQ.NEXTVAL, 2, '0'), V_DEPT_CD, E_DEPT_NAME);

    ELSIF E_TYPE = 'UPDATE' THEN
        SELECT COUNT(*) INTO V_NAME_COUNT
        FROM DEPT WHERE DEPT_NAME = E_DEPT_NAME AND DEPT_CD != E_DEPT_CD;
        IF V_NAME_COUNT > 0 THEN
            RAISE_APPLICATION_ERROR(-20001, '이미 존재하는 부서명입니다.');
        END IF;
        UPDATE DEPT SET DEPT_NAME = E_DEPT_NAME WHERE DEPT_CD = E_DEPT_CD;
        INSERT INTO DEPT_HISTORY(HISTORY_CD, DEPT_CD, DEPT_NAME)
        VALUES ('DH' || LPAD(DEPT_HISTORY_SEQ.NEXTVAL, 2, '0'), E_DEPT_CD, E_DEPT_NAME);

    ELSIF E_TYPE = 'DELETE' THEN
        INSERT INTO DEPT_HISTORY(HISTORY_CD, DEPT_CD, DEPT_NAME)
        SELECT 'DH' || LPAD(DEPT_HISTORY_SEQ.NEXTVAL, 2, '0'), DEPT_CD, DEPT_NAME
        FROM DEPT WHERE DEPT_CD = E_DEPT_CD;
        DELETE FROM DEPT WHERE DEPT_CD = E_DEPT_CD;
    END IF;

    COMMIT;
EXCEPTION
    WHEN OTHERS THEN ROLLBACK; RAISE;
END DEPT_HISTORY_CUD;
/

