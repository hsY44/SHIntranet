-- ── 2-1. 부서 ───────────────────────────────────────────────
CREATE TABLE DEPT (
     DEPT_CD   CHAR(4)        NOT NULL
    ,DEPT_NAME NVARCHAR2(20)  NOT NULL
    ,CONSTRAINT PK_DEPT PRIMARY KEY (DEPT_CD)
);

CREATE TABLE DEPT_HISTORY (
     HISTORY_CD CHAR(4)        NOT NULL
    ,DEPT_CD    CHAR(4)        NOT NULL
    ,DEPT_NAME  NVARCHAR2(20)  NOT NULL
    ,REG_DT     DATE           DEFAULT SYSDATE NOT NULL
    ,CONSTRAINT PK_DEPT_HISTORY PRIMARY KEY (HISTORY_CD)
);

-- ── 2-2. 직급 ───────────────────────────────────────────────
CREATE TABLE EMP_POSITION (
     POSITION_CD   CHAR(4)        NOT NULL
    ,POSITION_NAME NVARCHAR2(20)  NOT NULL
    ,GRADE         NUMBER         NOT NULL
    ,CONSTRAINT PK_EMP_POSITION PRIMARY KEY (POSITION_CD)
);

CREATE TABLE POSITION_HISTORY (
     HISTORY_CD    CHAR(4)        NOT NULL
    ,POSITION_CD   CHAR(4)        NOT NULL
    ,POSITION_NAME NVARCHAR2(20)  NOT NULL
    ,GRADE         NUMBER         NOT NULL
    ,REG_DT        DATE           DEFAULT SYSDATE NOT NULL
    ,CONSTRAINT PK_POSITION_HISTORY PRIMARY KEY (HISTORY_CD)
);

-- ── 2-3. 직원 ───────────────────────────────────────────────
CREATE TABLE EMP (
     EMP_CD      CHAR(6)        NOT NULL   -- 'SW' + 4자리 (EMP_SEQ)
    ,DEPT_CD     CHAR(4)        NOT NULL
    ,POSITION_CD CHAR(4)        NOT NULL
    ,EMP_NAME    NVARCHAR2(20)  NOT NULL
    ,REG_DT      DATE           DEFAULT SYSDATE NOT NULL
    ,CONSTRAINT PK_EMP PRIMARY KEY (EMP_CD)
);

CREATE TABLE EMP_INFO (
     EMP_CD    CHAR(6)        NOT NULL
    ,PWD       VARCHAR2(100)  NOT NULL
    ,TEL       VARCHAR2(11)
    ,EMAIL     VARCHAR2(100)
    ,ADDR      VARCHAR2(200)
    ,HIRE_DATE DATE           DEFAULT SYSDATE NOT NULL
    ,CONSTRAINT PK_EMP_INFO PRIMARY KEY (EMP_CD)
);

